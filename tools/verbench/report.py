def main() -> None:
    args = parse_args()
    input_path = Path(args.input).resolve() if args.input else default_input_path()
    output_path = Path(args.output).resolve() if args.output else derived_output_path(input_path)

    rows = load_results(input_path)
    if not rows:
        raise SystemExit("no versions found in input JSON")

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(render_html(rows, input_path), encoding="utf-8")
    print(f"HTML report written to {output_path}")


if __name__ == "__main__":
    main()
